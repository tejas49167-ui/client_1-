function parseDesignCsv(csvText) {
  const lines = csvText.split(/\r?\n/).filter((line) => line.trim());

  if (lines.length < 2) {
    return [];
  }

  const splitCsvLine = (line) => {
    const values = [];
    let value = "";
    let inQuotes = false;

    for (const character of line) {
      if (character === '"') {
        inQuotes = !inQuotes;
      } else if (character === "," && !inQuotes) {
        values.push(value.trim());
        value = "";
      } else {
        value += character;
      }
    }

    values.push(value.trim());
    return values;
  };

  const headers = splitCsvLine(lines[0]).map((header) => header.trim().toLowerCase());

  return lines.slice(1).map((line) => {
    const values = splitCsvLine(line);
    const row = {};

    headers.forEach((header, index) => {
      row[header] = values[index] || "";
    });

    return {
      name: row.name || row.title || "Design",
      photoUrl: row["photo url"] || row.photo || row.image || "",
      price: row.price || "",
    };
  });
}

function renderDesignCards(designs) {
  if (!designGrid) {
    return;
  }

  if (!designs.length) {
    designGrid.innerHTML = `
      <article class="design-card">
        <h2>No Designs Added Yet</h2>
        <p>Add rows to <code>products/products_list.csv</code> and they will appear here automatically.</p>
      </article>
    `;
    translatePage();
    return;
  }

  designGrid.innerHTML = designs
    .map((design) => {
      const safeName = escapeHtml(design.name || "Design");
      const safePrice = design.price ? `Rs. ${escapeHtml(design.price)}` : "Price on request";
      const safeRawPrice = escapeHtml(design.price || "");
      const safeRawPhotoUrl = escapeHtml(design.photoUrl || "");
      const safePhotoUrl = escapeCssUrl(resolveLocalPath(design.photoUrl));
      const imageMarkup = design.photoUrl
        ? `<div class="design-preview design-photo" style="background-image: linear-gradient(rgba(76, 47, 40, 0.08), rgba(76, 47, 40, 0.08)), url('${safePhotoUrl}');"></div>`
        : `<div class="design-preview preview-one"></div>`;
      const cartMode = designGrid.dataset.cartMode || "login";
      const cartAction =
        cartMode === "cart"
          ? `
          <form class="cart-form" action="/cart/add" method="post">
            <input type="hidden" name="design_name" value="${safeName}" />
            <input type="hidden" name="price" value="${safeRawPrice}" />
            <input type="hidden" name="photo_url" value="${safeRawPhotoUrl}" />
            <button class="primary-btn" type="submit">Add To Cart</button>
          </form>
          `
          : `
          <div class="cart-form">
            <a class="primary-btn" href="/login">Add To Cart</a>
          </div>
          `;

      return `
        <article class="design-card">
          ${imageMarkup}
          <h2>${safeName}</h2>
          <p class="design-price">${safePrice}</p>
          ${cartAction}
        </article>
      `;
    })
    .join("");

  translatePage();
}

function filterDesigns() {
  const query = designSearch?.value.trim().toLowerCase() || "";
  const filteredDesigns = allDesigns.filter((design) => {
    const searchableText = `${design.name} ${design.price}`.toLowerCase();
    return searchableText.includes(query);
  });

  if (query && !filteredDesigns.length) {
    designGrid.innerHTML = `
      <article class="design-card">
        <h2>No Matching Designs</h2>
        <p>Try a different design name or price.</p>
      </article>
    `;
    translatePage();
    return;
  }

  renderDesignCards(filteredDesigns);
}

async function loadDesigns() {
  if (!designGrid) {
    return;
  }

  try {
    const response = await fetch("/products/products_list.csv");

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`);
    }

    const csvText = await response.text();
    allDesigns = parseDesignCsv(csvText);
    filterDesigns();
  } catch (error) {
    designGrid.innerHTML = `
      <article class="design-card">
        <h2>Unable To Load Designs</h2>
        <p>Open the site through Flask so the browser can read <code>products/products_list.csv</code>.</p>
      </article>
    `;
    translatePage();
  }
}

designSearch?.addEventListener("input", filterDesigns);
renderFlashMessages();
initHomeSlider();
translatePage();
loadDesigns();
