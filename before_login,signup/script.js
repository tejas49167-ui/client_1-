function translatePage(lang = currentLanguage()) {
  document.documentElement.lang = lang === "kn" ? "kn" : "en";

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.langOption === lang);
    button.setAttribute("aria-pressed", String(button.dataset.langOption === lang));
  });

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE", "CODE"].includes(parent.tagName)) {
        return NodeFilter.FILTER_REJECT;
      }

      return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    },
  });

  const textNodes = [];
  while (walker.nextNode()) {
    textNodes.push(walker.currentNode);
  }

  textNodes.forEach((node) => {
    node.originalText ??= node.nodeValue.trim();
    const translated = translateText(node.originalText, lang);
    node.nodeValue = node.nodeValue.replace(node.nodeValue.trim(), translated);
  });

  document.querySelectorAll("input[placeholder]").forEach((input) => {
    input.dataset.originalPlaceholder ??= input.getAttribute("placeholder");
    const translated = placeholderTranslations[lang]?.[input.dataset.originalPlaceholder] || input.dataset.originalPlaceholder;
    input.setAttribute("placeholder", translated);
  });
}

document.querySelectorAll("[data-lang-option]").forEach((button) => {
  button.addEventListener("click", () => {
    localStorage.setItem("siteLanguage", button.dataset.langOption);
    translatePage(button.dataset.langOption);
  });
});

